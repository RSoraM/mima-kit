const ROUND = [0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1b, 0x36];

// * Functions

function GFMultiply(a: number, b: number): number {
  let p = 0;
  if (b === 1) return a;
  for (let i = 0; i < 8; i++) {
    if (b & 1) p ^= a;
    const carry = a & 0x80;
    a <<= 1;
    // 0x1B 是不可约多项式 x^8 + x^4 + x^3 + x + 1 的低 8 位
    if (carry) a ^= 0x1b;
    b >>= 1;
  }
  return p & 0xff;
}

function KeyExpansion(K: Uint8Array, Nr: 10 | 12 | 14) {
  const Nk = K.byteLength >> 2;
  const W = new Uint8Array((Nr + 1) << 4);
  W.set(K);
  let current = 0;
  for (let i = Nk; i < (Nr + 1) << 2; i++) {
    const i_1 = (i - 1) << 2;
    const temp = W.slice(i_1, i_1 + 4);
    if (i % Nk === 0) {
      const t0 = temp[0];
      temp[0] = SBox[temp[1]] ^ ROUND[current];
      temp[1] = SBox[temp[2]];
      temp[2] = SBox[temp[3]];
      temp[3] = SBox[t0];

      current++;
    } else if (Nk > 6 && i % Nk === 4) {
      temp[0] = SBox[temp[0]];
      temp[1] = SBox[temp[1]];
      temp[2] = SBox[temp[2]];
      temp[3] = SBox[temp[3]];
    }
    const i_Nk = (i - Nk) << 2;
    const Wi_NK = W.subarray(i_Nk, i_Nk + 4);
    for (let j = 0; j < 4; j++) {
      temp[j] ^= Wi_NK[j];
    }
    W.set(temp, i << 2);
  }
  return W;
}

// * AES Algorithm

function Cipher(M: Uint8Array, W: Uint8Array, Nr: 10 | 12 | 14) {
  if (M.byteLength !== 16) {
    throw new KitError(`AES block must be 16 byte`);
  }
  const S = U8.from(M);

  const AddRoundKey = (W: Uint8Array) => {
    for (let i = 0; i < S.byteLength; i++) {
      S[i] ^= W[i];
    }
  };
  const SubBytes = () => {
    for (let i = 0; i < S.byteLength; i++) {
      S[i] = SBox[S[i]];
    }
  };
  const ShiftRows = () => {
    const S1 = S[1];
    S[1] = S[5];
    S[5] = S[9];
    S[9] = S[13];
    S[13] = S1;

    const S2 = S[2];
    const S6 = S[6];
    S[2] = S[10];
    S[6] = S[14];
    S[10] = S2;
    S[14] = S6;

    const S15 = S[15];
    S[15] = S[11];
    S[11] = S[7];
    S[7] = S[3];
    S[3] = S15;
  };
  const MixColumn = () => {
    for (let i = 0; i < 4; i++) {
      const s0 = S[i << 2];
      const s1 = S[(i << 2) + 1];
      const s2 = S[(i << 2) + 2];
      const s3 = S[(i << 2) + 3];

      const t0 = GFMultiply(s0, 0x02) ^ GFMultiply(s1, 0x03) ^ GFMultiply(s2, 0x01) ^ GFMultiply(s3, 0x01);
      const t1 = GFMultiply(s0, 0x01) ^ GFMultiply(s1, 0x02) ^ GFMultiply(s2, 0x03) ^ GFMultiply(s3, 0x01);
      const t2 = GFMultiply(s0, 0x01) ^ GFMultiply(s1, 0x01) ^ GFMultiply(s2, 0x02) ^ GFMultiply(s3, 0x03);
      const t3 = GFMultiply(s0, 0x03) ^ GFMultiply(s1, 0x01) ^ GFMultiply(s2, 0x01) ^ GFMultiply(s3, 0x02);

      S[i << 2] = t0;
      S[(i << 2) + 1] = t1;
      S[(i << 2) + 2] = t2;
      S[(i << 2) + 3] = t3;
    }
  };

  AddRoundKey(W.subarray(0, 16));
  for (let i = 1; i < Nr; i++) {
    SubBytes();
    ShiftRows();
    MixColumn();
    AddRoundKey(W.subarray(i << 4, (i + 1) << 4));
  }
  SubBytes();
  ShiftRows();
  AddRoundKey(W.subarray(W.length - 16, W.length));

  return S;
}

function InvCipher(M: Uint8Array, W: Uint8Array, Nr: 10 | 12 | 14) {
  if (M.byteLength !== 16) {
    throw new KitError(`AES block must be 16 byte`);
  }
  const S = new U8(M.slice(0));

  const AddRoundKey = (W: Uint8Array) => {
    for (let i = 0; i < S.byteLength; i++) {
      S[i] ^= W[i];
    }
  };
  const InvSubBytes = () => {
    for (let i = 0; i < S.byteLength; i++) {
      S[i] = InvSBox[S[i]];
    }
  };
  const InvShiftRows = () => {
    const S13 = S[13];
    S[13] = S[9];
    S[9] = S[5];
    S[5] = S[1];
    S[1] = S13;

    const S2 = S[2];
    const S6 = S[6];
    S[2] = S[10];
    S[6] = S[14];
    S[10] = S2;
    S[14] = S6;

    const S3 = S[3];
    S[3] = S[7];
    S[7] = S[11];
    S[11] = S[15];
    S[15] = S3;
  };
  const InvMixColumn = () => {
    for (let i = 0; i < 4; i++) {
      const s0 = S[i << 2];
      const s1 = S[(i << 2) + 1];
      const s2 = S[(i << 2) + 2];
      const s3 = S[(i << 2) + 3];

      const t0 = GFMultiply(s0, 0x0e) ^ GFMultiply(s1, 0x0b) ^ GFMultiply(s2, 0x0d) ^ GFMultiply(s3, 0x09);
      const t1 = GFMultiply(s0, 0x09) ^ GFMultiply(s1, 0x0e) ^ GFMultiply(s2, 0x0b) ^ GFMultiply(s3, 0x0d);
      const t2 = GFMultiply(s0, 0x0d) ^ GFMultiply(s1, 0x09) ^ GFMultiply(s2, 0x0e) ^ GFMultiply(s3, 0x0b);
      const t3 = GFMultiply(s0, 0x0b) ^ GFMultiply(s1, 0x0d) ^ GFMultiply(s2, 0x09) ^ GFMultiply(s3, 0x0e);

      S[i << 2] = t0;
      S[(i << 2) + 1] = t1;
      S[(i << 2) + 2] = t2;
      S[(i << 2) + 3] = t3;
    }
  };

  AddRoundKey(W.subarray(W.length - 16, W.length));
  for (let i = Nr - 1; i > 0; i--) {
    InvShiftRows();
    InvSubBytes();
    AddRoundKey(W.subarray(i << 4, (i + 1) << 4));
    InvMixColumn();
  }
  InvShiftRows();
  InvSubBytes();
  AddRoundKey(W.subarray(0, 16));

  return S;
}

function _aes(K: Uint8Array, b: 128 | 192 | 256) {
  if (K.byteLength !== b >> 3) {
    throw new KitError(`AES key must be ${b >> 3} byte`);
  }
  const Nr = b === 128 ? 10 : b === 192 ? 12 : 14;
  const W = KeyExpansion(K, Nr);

  return {
    encrypt: (M: Uint8Array) => Cipher(M, W, Nr),
    decrypt: (C: Uint8Array) => InvCipher(C, W, Nr),
  };
}

/**
 * 高级加密标准 (AES) 分组密码算法
 *
 * Advanced Encryption Standard (AES) block cipher algorithm
 *
 * @param {128 | 192 | 256} b - 密钥长度 / Key size (bit)
 */
export function aes(b: 128 | 192 | 256) {
  return createCipher((K: Uint8Array) => _aes(K, b), {
    ALGORITHM: `AES-${b}`,
    BLOCK_SIZE: 16,
    KEY_SIZE: b >> 3,
    MIN_KEY_SIZE: b >> 3,
    MAX_KEY_SIZE: b >> 3,
  });
}
