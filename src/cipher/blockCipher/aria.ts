function KeyScheduling(K: Uint8Array, round: 12 | 14 | 16) {
  const _K = new Uint8Array(32);
  _K.set(K);
  K = _K;
  const KL = K.subarray(0, 16);
  const KR = K.subarray(16, 32);

  let CK1: Uint8Array, CK2: Uint8Array, CK3: Uint8Array;
  switch (round) {
    case 12:
      CK1 = C1;
      CK2 = C2;
      CK3 = C3;
      break;
    case 14:
      CK1 = C2;
      CK2 = C3;
      CK3 = C1;
      break;
    case 16:
      CK1 = C3;
      CK2 = C1;
      CK3 = C2;
      break;
  }

  const W0 = new Uint8Array(16);
  const W1 = new Uint8Array(16);
  const W2 = new Uint8Array(16);
  const W3 = new Uint8Array(16);
  W0.set(KL);
  W1.set(FO(W0, CK1));
  W1.forEach((_, i) => {
    W1[i] ^= KR[i];
  });
  W2.set(FE(W1, CK2));
  W2.forEach((_, i) => {
    W2[i] ^= W0[i];
  });
  W3.set(FO(W2, CK3));
  W3.forEach((_, i) => {
    W3[i] ^= W1[i];
  });
  const EK = computeEK([W0, W1, W2, W3], round);
  const DK = computeDK(EK, round);

  return { EK, DK };
}
function computeEK(W: Uint8Array[], round: 12 | 14 | 16) {
  const [W0, W1, W2, W3] = W;
  // ek1  = W0 ^ (W1 >>> 19)
  // ek2  = W1 ^ (W2 >>> 19)
  // ek3  = W2 ^ (W3 >>> 19)
  // ek4  = W3 ^ (W0 >>> 19)
  // ek5  = W0 ^ (W1 >>> 31)
  // ek6  = W1 ^ (W2 >>> 31)
  // ek7  = W2 ^ (W3 >>> 31)
  // ek8  = W3 ^ (W0 >>> 31)
  // ek9  = W0 ^ (W1 <<< 61)
  // ek10 = W1 ^ (W2 <<< 61)
  // ek11 = W2 ^ (W3 <<< 61)
  // ek12 = W3 ^ (W0 <<< 61)
  // ek13 = W0 ^ (W1 <<< 31)
  // ek14 = W1 ^ (W2 <<< 31)
  // ek15 = W2 ^ (W3 <<< 31)
  // ek16 = W3 ^ (W0 <<< 31)
  // ek17 = W0 ^ (W1 <<< 19)
  const ek01 = XOR(RR128(W1, 19), W0);
  const ek02 = XOR(RR128(W2, 19), W1);
  const ek03 = XOR(RR128(W3, 19), W2);
  const ek04 = XOR(RR128(W0, 19), W3);
  const ek05 = XOR(RR128(W1, 31), W0);
  const ek06 = XOR(RR128(W2, 31), W1);
  const ek07 = XOR(RR128(W3, 31), W2);
  const ek08 = XOR(RR128(W0, 31), W3);
  const ek09 = XOR(RL128(W1, 61), W0);
  const ek10 = XOR(RL128(W2, 61), W1);
  const ek11 = XOR(RL128(W3, 61), W2);
  const ek12 = XOR(RL128(W0, 61), W3);
  const ek13 = XOR(RL128(W1, 31), W0);
  if (round === 12) {
    return [ek01, ek02, ek03, ek04, ek05, ek06, ek07, ek08, ek09, ek10, ek11, ek12, ek13];
  }
  const ek14 = XOR(RL128(W2, 31), W1);
  const ek15 = XOR(RL128(W3, 31), W2);
  if (round === 14) {
    return [ek01, ek02, ek03, ek04, ek05, ek06, ek07, ek08, ek09, ek10, ek11, ek12, ek13, ek14, ek15];
  }
  const ek16 = XOR(RL128(W0, 31), W3);
  const ek17 = XOR(RL128(W1, 19), W0);
  return [ek01, ek02, ek03, ek04, ek05, ek06, ek07, ek08, ek09, ek10, ek11, ek12, ek13, ek14, ek15, ek16, ek17];
}
function computeDK(EK: Uint8Array[], round: 12 | 14 | 16) {
  const DK = Array.from<Uint8Array>({ length: EK.length });
  // dk1 = ek{ n + 1 },
  // dk2 = A(ek{ n }),
  // dk3 = A(ek{ n- 1}),
  // ...,
  // dk{ n }= A(ek2),
  // dk{ n + 1 }= ek1.
  DK[0] = EK[round];
  let j = round - 1;
  for (let i = 1; i < round; i++) {
    DK[i] = A(EK[j--]);
  }
  DK[round] = EK[0];
  return DK;
}

// * ARIA Algorithm

function _aria(K: Uint8Array, b: 128 | 192 | 256) {
  if (K.byteLength !== b >> 3) {
    throw new KitError(`Aria-${b} key must be ${b >> 3} byte`);
  }
  /**
   * - 128-bit key: 12 rounds
   * - 192-bit key: 14 rounds
   * - 256-bit key: 16 rounds
   */
  const round = b === 128 ? 12 : b === 192 ? 14 : 16;
  const { EK, DK } = KeyScheduling(K, round);
  const cipher = (M: Uint8Array, RK: Uint8Array[]) => {
    if (M.byteLength !== 16) {
      throw new KitError('ARIA block must be 16 byte');
    }
    let P = M;
    let i = 0;
    while (i < round - 2) {
      P = FO(P, RK[i++]);
      P = FE(P, RK[i++]);
    }
    P = FO(P, RK[i++]);
    P = SL2(XOR(P, RK[i++]));
    P = XOR(P, RK[i++]);
    return new U8(P);
  };
  return {
    encrypt: (M: Uint8Array) => cipher(M, EK),
    decrypt: (C: Uint8Array) => cipher(C, DK),
  };
}

/**
 * ARIA 分组密码算法 / block cipher algorithm
 *
 * @param {128 | 192 | 256} b - 密钥长度 / Key size (bit)
 */
export function aria(b: 128 | 192 | 256) {
  return createCipher((K: Uint8Array) => _aria(K, b), {
    ALGORITHM: `ARIA-${b}`,
    BLOCK_SIZE: 16,
    KEY_SIZE: b >> 3,
    MIN_KEY_SIZE: 16,
    MAX_KEY_SIZE: 32,
  });
}
